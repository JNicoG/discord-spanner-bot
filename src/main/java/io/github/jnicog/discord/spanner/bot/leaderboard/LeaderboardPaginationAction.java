package io.github.jnicog.discord.spanner.bot.leaderboard;

/**
 * Represents the pagination actions available for navigating leaderboard pages.
 */
public enum LeaderboardPaginationAction {
    /**
     * Navigate to the first page of the leaderboard.
     */
    FIRST_PAGE,

    /**
     * Navigate to the previous page of the leaderboard.
     */
    PREVIOUS_PAGE,

    /**
     * Navigate to the next page of the leaderboard.
     */
    NEXT_PAGE,

    /**
     * Navigate to the last page of the leaderboard.
     */
    LAST_PAGE
}

